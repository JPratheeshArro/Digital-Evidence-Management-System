        category: 'user_behavior',
        priority: 'medium',
        urgency: 'normal',
        impact: 'security',
        recommendations: userActivity.recommendations,
        actions: [
          { type: 'review', label: 'Review Activity' },
          { type: 'contact', label: 'Contact User' }
        ],
        escalation: { level: 'normal', notifyAdmins: false },
        notifications: { email: true, sms: false, push: true },
