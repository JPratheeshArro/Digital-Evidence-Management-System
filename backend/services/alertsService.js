  /**
   * Get alert statistics
   * @param {Object} filters - Statistics filters
   * @returns {Object} Alert statistics
   */
  static async getAlertStatistics(filters = {}) {
    try {
      const alertReport = await this.generateIntelligentAlerts(filters);
      const alerts = alertReport.alerts;

      const statistics = {
        total: alerts.length,
        bySeverity: this.groupAlertsByField(alerts, 'severity'),
        byCategory: this.groupAlertsByField(alerts, 'category'),
        byType: this.groupAlertsByField(alerts, 'type'),
        byStatus: this.groupAlertsByField(alerts, 'status'),
        averageRiskScore: alerts.length > 0 
          ? (alerts.reduce((sum, alert) => sum + (alert.details.riskScore || 0), 0) / alerts.length).toFixed(1)
          : 0,
        trends: await this.calculateAlertTrends(),
        topUsers: await this.getTopUsersByAlerts(alerts),
        resolutionTime: this.calculateAverageResolutionTime(alerts)
      };

      return statistics;
    } catch (error) {
      throw new Error(`Error getting alert statistics: ${error.message}`);
    }
  }

  /**
   * Helper methods
   */
  static generateAlertId() {
    return `alert_${Date.now()}_${Math.random().toString(36).substr(2, 9)}`;
  }

  static filterAnomaliesBySeverity(anomalies, minSeverity) {
    const severityLevels = { low: 1, medium: 2, high: 3, critical: 4 };
    const minLevel = severityLevels[minSeverity] || 1;
    
    return anomalies.filter(anomaly => 
      severityLevels[anomaly.severity] >= minLevel
    );
  }

  static generateAlertTitle(anomaly) {
    const titles = {
      'duplicate_evidence': 'Duplicate Evidence Detected',
      'cross_case_usage': 'Cross-Case Evidence Usage Alert',
      'bulk_upload_pattern': 'Bulk Upload Pattern Alert',
      'rapid_upload_pattern': 'Rapid Upload Pattern Alert'
    };
    
    return titles[anomaly.type] || 'Anomaly Detected';
  }

  static getAlertCategory(type) {
    const categories = {
      'duplicate_evidence': 'data_integrity',
      'cross_case_usage': 'evidence_management',
      'bulk_upload_pattern': 'user_behavior',
      'rapid_upload_pattern': 'user_behavior'
    };
    
    return categories[type] || 'other';
  }

  static getAlertPriority(severity) {
    const priorities = {
      'critical': 'P0',
      'high': 'P1',
      'medium': 'P2',
      'low': 'P3'
    };
    
    return priorities[severity] || 'P4';
  }

  static getAlertUrgency(riskScore) {
    if (riskScore >= 80) return 'immediate';
    if (riskScore >= 60) return 'urgent';
    if (riskScore >= 40) return 'normal';
    return 'low';
  }

  static calculateAlertImpact(anomaly) {
    const impacts = {
      'duplicate_evidence': 'data_integrity',
      'cross_case_usage': 'evidence_contamination',
      'bulk_upload_pattern': 'system_load',
      'rapid_upload_pattern': 'security_risk'
    };
    
    return impacts[anomaly.type] || 'unknown';
  }

